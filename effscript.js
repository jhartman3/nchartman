function showAll(){
	$(document).ready(function(){
		$("div").show();
	})
}

function hideAll(){
	$(document).ready(function(){
		$("div").hide();
	})
}

function fadeSwitch(){
	$(document).ready(function(){
		$("div").fadeToggle(500);
	})
}

function slideSwitch(){
	$(document).ready(function(){
		$("div").slideToggle(500);
	})
}

function displaySwitch(){
	$(document).ready(function(){
		$("div").toggle(500);
	})
}